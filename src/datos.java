public class datos {
    public static void main(String[] args) {
        byte tipobyte=17;
        System.out.println("tipobyte = " + tipobyte);
        short k=23457;
        System.out.println("k = " + k);
        int tipoint=123456789;
        System.out.println("tipoint = " + tipoint);
        long tipolong=123467875567L;
        System.out.println("tipolong = " + tipolong);
        //valor punto flotante (0.0)
        float tipoflotante= 3.14F;
        System.out.println("tipoflotante = " + tipoflotante);
        double tipodouble=3.134353;
        System.out.println("tipodouble = " + tipodouble);
        //caracter (')
        char tipochar='A';
        System.out.println("tipochar = " + tipochar);
        tipochar = 66;
        System.out.println("tipochar = " + tipochar);
        //booleano
        boolean tipobooleano=true;
        System.out.println("tipobooleano = " + tipobooleano);
        //tipo object(referencia)
        String nombre=null;
        System.out.println("nombre = " + nombre);

    }
}
